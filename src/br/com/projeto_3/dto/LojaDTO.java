package br.com.projeto_3.dto;
import java.util.Date;

public class LojaDTO {
    
    private String nome_loja, cnpj_loja, tel_loja, rua_loja, bairro_loja, cep_loja;
    private int num_loja;
    private int id_loja;

    /**
     * @return the nome_loja
     */
    public String getNome_loja() {
        return nome_loja;
    }

    /**
     * @param nome_loja the nome_loja to set
     */
    public void setNome_loja(String nome_loja) {
        this.nome_loja = nome_loja;
    }

    /**
     * @return the cnpj_loja
     */
    public String getCnpj_loja() {
        return cnpj_loja;
    }

    /**
     * @param cnpj_loja the cnpj_loja to set
     */
    public void setCnpj_loja(String cnpj_loja) {
        this.cnpj_loja = cnpj_loja;
    }

    /**
     * @return the tel_loja
     */
    public String getTel_loja() {
        return tel_loja;
    }

    /**
     * @param tel_loja the tel_loja to set
     */
    public void setTel_loja(String tel_loja) {
        this.tel_loja = tel_loja;
    }

    /**
     * @return the rua_loja
     */
    public String getRua_loja() {
        return rua_loja;
    }

    /**
     * @param rua_loja the rua_loja to set
     */
    public void setRua_loja(String rua_loja) {
        this.rua_loja = rua_loja;
    }

    public String getBairro_loja() {
        return bairro_loja;
    }

    /**
     * @param bairro_loja the bairro_loja to set
     */
    public void setBairro_loja(String bairro_loja) {
        this.bairro_loja = bairro_loja;
    }

    /**
     * @return the cep_loja
     */
    public String getCep_loja() {
        return cep_loja;
    }

    /**
     * @param cep_loja the cep_loja to set
     */
    public void setCep_loja(String cep_loja) {
        this.cep_loja = cep_loja;
    }

    /**
     * @return the id_loja
     */
    public int getId_loja() {
        return id_loja;
    }

    /**
     * @param id_loja the id_loja to set
     */
    public void setId_loja(int id_loja) {
        this.id_loja = id_loja;
    }

    /**
     * @return the num_loja
     */
    public int getNum_loja() {
        return num_loja;
    }

    /**
     * @param num_loja the num_loja to set
     */
    public void setNum_loja(int num_loja) {
        this.num_loja = num_loja;
    }

    
    
}
